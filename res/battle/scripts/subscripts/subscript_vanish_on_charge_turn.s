    .include "macros/btlcmd.inc"

    .data

_000:
    PlayMoveAnimation BTLSCR_ATTACKER
    Wait 
    PrintBufferedMessage 
    Wait 
    WaitButtonABTime 30
	CompareVarToValue OPCODE_EQU, BTLVAR_MOVE_TEMP, MOVE_SOLAR_BEAM, _027
	LockMoveChoice BTLSCR_SIDE_EFFECT_MON
    CompareMonDataToValue OPCODE_FLAG_NOT, BTLSCR_SIDE_EFFECT_MON, BATTLEMON_MOVE_EFFECTS_MASK, MOVE_EFFECT_SEMI_INVULNERABLE, _017
    ToggleVanish BTLSCR_SIDE_EFFECT_MON, TRUE
	CompareVarToValue OPCODE_EQU, BTLVAR_MOVE_TEMP, MOVE_SHADOW_FORCE, _037
	
_017:
    End
	
_027:
	UpdateVar OPCODE_FLAG_ON, BTLVAR_BATTLE_CTX_STATUS_2, SYSCTL_STAT_STAGE_CHANGE_SHOWN
    UpdateVar OPCODE_SET, BTLVAR_SIDE_EFFECT_PARAM, MOVE_SUBSCRIPT_PTR_SP_ATTACK_UP_1_STAGE
    Call BATTLE_SUBSCRIPT_UPDATE_STAT_STAGE
    UpdateVar OPCODE_FLAG_OFF, BTLVAR_BATTLE_CTX_STATUS_2, SYSCTL_UPDATE_STAT_STAGES
    UpdateVar OPCODE_FLAG_OFF, BTLVAR_BATTLE_CTX_STATUS_2, SYSCTL_STAT_STAGE_CHANGE_SHOWN
	LockMoveChoice BTLSCR_SIDE_EFFECT_MON
    CompareMonDataToValue OPCODE_FLAG_NOT, BTLSCR_SIDE_EFFECT_MON, BATTLEMON_MOVE_EFFECTS_MASK, MOVE_EFFECT_SEMI_INVULNERABLE, _017
    ToggleVanish BTLSCR_SIDE_EFFECT_MON, TRUE
	End

_037:
	CompareMonDataToValue OPCODE_NEQ, BTLSCR_SIDE_EFFECT_MON, BATTLEMON_HEAL_INVERSION_TURNS, 0, _047
	UpdateMonDataFromVar OPCODE_GET, BTLSCR_SIDE_EFFECT_MON, BATTLEMON_MAX_HP, BTLVAR_CALC_TEMP
	CompareMonDataToVar OPCODE_EQU, BTLSCR_SIDE_EFFECT_MON, BATTLEMON_CUR_HP, BTLVAR_CALC_TEMP, _017
	UpdateMonDataFromVar OPCODE_GET, BTLSCR_SIDE_EFFECT_MON, BATTLEMON_MAX_HP, BTLVAR_HP_CALC_TEMP
	
    DivideVarByValue BTLVAR_HP_CALC_TEMP, 4
    UpdateVarFromVar OPCODE_SET, BTLVAR_MSG_BATTLER_TEMP, BTLVAR_ATTACKER
	UpdateVar OPCODE_FLAG_ON, BTLVAR_BATTLE_CTX_STATUS, SYSCTL_SKIP_SPRITE_BLINK
    Call BATTLE_SUBSCRIPT_UPDATE_HP
	End

_047:
	DivideVarByValue BTLVAR_HP_CALC_TEMP, 4
	UpdateVar OPCODE_MUL, BTLVAR_HP_CALC_TEMP, -1
    UpdateVarFromVar OPCODE_SET, BTLVAR_MSG_BATTLER_TEMP, BTLVAR_ATTACKER
	UpdateVar OPCODE_FLAG_ON, BTLVAR_BATTLE_CTX_STATUS, SYSCTL_SKIP_SPRITE_BLINK
    Call BATTLE_SUBSCRIPT_UPDATE_HP
	End